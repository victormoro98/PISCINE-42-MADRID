#include <stdio.h>

int	main()
{
	char arg[] = "4 3 2 1 1 2 2 2 4 3 2 1 1 2 2 2";
	
	int array[4][4] = 
	{
		{1, 2, 3, 4},
		{2, 3, 4, 1},
		{3, 4, 1, 2},
		{4, 1, 2, 3},
		
	};

	int n = 0;
	int x = 0;
	int y = 0;
	int raro = 0;
	while (x <= 4)
	{
		while (y <= 4)
		{
			if (array[x][y] < array[x + 1][y])
			{
				n++;
				x++;
				raro = arg[2 * n] - 48;
				if (raro == n)
					return (0);
			}
		}	
	}
}
