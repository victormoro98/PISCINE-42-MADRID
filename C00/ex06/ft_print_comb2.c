#include <unistd.h>

void	ft_print_comb2(void)
{
	int		i;
	int		j;
	char	a[5];

	i = 0;
	while (i <= 99)
	{
		j = i + 1;
		while (j <= 99)
		{
			a[0] = i / 10 % 10 + '0';
			a[1] = i % 10 + '0';
			a[2] = ' ';
			a[3] = j / 10 % 10 + '0';
			a[4] = j % 10 + '0';
			write (1, a, 5);
			if (i == 98 && j == 99)
				write (1, "\n", 2);
			else
				write (1, ", ", 1);
			j++;
		}
		i++;
	}
}

int	main(void)
{
	ft_print_comb2();
	return (0);
}
